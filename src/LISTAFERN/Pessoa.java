public class Pessoa {

    private String nome;
    private String sexo;
    private int idada;


     public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public int getIdada() {
        return idada;
    }

    public void setIdada(int idada) {
        this.idada = idada;
    }
}
